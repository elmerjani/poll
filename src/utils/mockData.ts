export function getTotalVotes(options: { votes: number }[]) {
  return options.reduce((sum, opt) => sum + opt.votes, 0);
}

export function sortOptionsByVotes(options: { id: string; text: string; votes: number }[]) {
  return [...options].sort((a, b) => b.votes - a.votes);
}
